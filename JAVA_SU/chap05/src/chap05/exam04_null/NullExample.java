package chap05.exam04_null;

public class NullExample {

	public static void main(String[] args) {
		Object refVar1 = 20;
		Object refVar2 = null;
		
		System.out.println(refVar1 == null);
		System.out.println(refVar1 != null);
		
		System.out.println(refVar2 == null);
		System.out.println(refVar2 != null);
		

	}

}
