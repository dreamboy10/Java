package chap05.exam01_datatype;

public class DataTypeExample {

	public static void main(String[] args) {
		int age = 25;
		double price = 100.5;
		
		System.out.println(age);
		System.out.println(price);
		
		String name = "이한정";
		String hobby = "독서";
		
		System.out.println(name);
		System.out.println(hobby);

	}

}
