package org.dreamboy.javatutorials.Array;

public class LengthDemo {

	public static void main(String[] args) {
		String[] classGroup = new String[4];
        classGroup[0] = "이한정";
        System.out.println(classGroup.length);
        classGroup[1] = "서정원";
        System.out.println(classGroup.length);
        classGroup[2] = "곽경문";
        System.out.println(classGroup.length);
        classGroup[3] = "드림보이";
        System.out.println(classGroup.length);
	}

}
