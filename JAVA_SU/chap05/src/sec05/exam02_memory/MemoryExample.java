package sec05.exam02_memory;

public class MemoryExample {

	public static void main(String[] args) {
		char v1 = 'A';
		
		System.out.println(v1);
		
		if(v1 == 'A') {
			int v2 = 100;
			double v3 = 3.14;
			
			System.out.println(v2);
			System.out.println(v3);
		}
		
		boolean v4 = true;
		
		System.out.println(v4);

	}

}
