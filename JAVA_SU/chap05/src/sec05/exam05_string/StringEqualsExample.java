package sec05.exam05_string;

public class StringEqualsExample {

	public static void main(String[] args) {
		String strVar1 = "드림보이";
		String strVar2 = "드림보이";
		
		if(strVar1 == strVar2) {
			System.out.println
			("strVar1과 strVar2는 "
			+ "참조가 같음");
		} else {
			System.out.println
			("strVar1과 strVar2는 "
			+ "참조가 다름");
		}
		
		String strVar3 = 
		new String("드림보이");
		String strVar4 = 
		new String("드림보이");
		
		if(strVar3 == strVar4) {
			System.out.println
			("strVar3과 strVar4는 "
			+ "참조가 같음");
		} else {
			System.out.println
			("strVar3과 strVar4는 "
			+ "참조가 다름");
		}
		
		if(strVar3.equals(strVar4)) {
			System.out.println
			("strVar3과 strVar4는 "
			+ "문자열이 같음");
		}

	}

}
